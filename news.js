const newsContainer=document.querySelector(".news-container")
const allBtn=document.querySelector(".all")
const e1Sem1Btn=document.querySelector(".E1-Sem1")
const e1Sem2Btn=document.querySelector(".E1-Sem2")

const toggleBtn=document.querySelector(".toggle-btn")
const crossBtn=document.querySelector(".cross-btn")
const navbar=document.querySelector(".navbar")
const menu=document.querySelector(".menu")
const topbtn=document.querySelector(".toplink")
const btmbtn=document.querySelector('.btmlink')

window.addEventListener("DOMContentLoaded",function(){

    var str=news.map(function(singlenews){
        return `
        <img src="${singlenews.src}" alt="${singlenews.date}" title="${singlenews.date}">
    
        `
    })
    str=str.join("")
    newsContainer.innerHTML=str
})

allBtn.addEventListener("click",function(){
    var str=news.map(function(singlenews){
        return `
        <img src="${singlenews.src}" alt="${singlenews.date}" title="${singlenews.date}" class="single-image">
        `
    })
    str=str.join("")
    newsContainer.innerHTML=str
})

e1Sem1Btn.addEventListener('click',function(){
    var str=news.map(function(singlenews){
        if(singlenews.category=="E1-Sem1")
        return `
        <img src="${singlenews.src}" alt="${singlenews.date}" title="${singlenews.date}">
       
        `
    })
    str=str.join("")
    newsContainer.innerHTML=str
})
e1Sem2Btn.addEventListener('click',function(){
    var str=news.map(function(singlenews){
        if(singlenews.category=="E1-Sem2")
        return `
        <img src="${singlenews.src}" alt="${singlenews.date}" title="${singlenews.date}">
        `
    })
    str=str.join("")
    newsContainer.innerHTML=str
})

window.addEventListener('scroll',function(){
    var pageh=this.window.pageYOffset
    if(pageh>100){
        topbtn.style.visibility="visible"
        btmbtn.style.visibility="visible"
    }
    else{
        topbtn.style.visibility="hidden"
        btmbtn.style.visibility="hidden"

    }
})

toggleBtn.addEventListener("click",function(){
    menu.classList.add("active")
    toggleBtn.style.visibility="hidden"
    crossBtn.style.visibility="visible"
})
crossBtn.addEventListener("click",function(){
    menu.classList.remove("active")
    toggleBtn.style.visibility="visible"
    crossBtn.style.visibility="hidden"

})
